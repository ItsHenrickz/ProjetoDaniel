function abrirModal1(){
    const modal = document.getElementById('janela-modal1')
    modal.classList.add('abrir')

    modal.addEventListener('click', (e) => {
        if(e.target.id == 'fechar' || e.target.id == 'janela-modal1'){
            modal.classList.remove('abrir')
        }
    })
}

